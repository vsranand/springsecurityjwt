package com.nttdata.springsecurityjwt.domain;

public class Scores {

	private String factor;

	private Float score;

	public String getFactor() {
		return factor;
	}

	public void setFactor(String factor) {
		this.factor = factor;
	}

	public Float getScore() {
		return score;
	}

	public void setScore(Float score) {
		this.score = score;
	}

}
